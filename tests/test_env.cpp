void createFramebuffers()
{
    swapChainFramebuffers.resize(env->swapChainImageViews.size());
    for (size_t i = 0; i < env->swapChainImageViews.size(); i++)
    {
        VkImageView attachments[] = {env->swapChainImageViews[i]};

        VkFramebufferCreateInfo framebufferInfo{};
        framebufferInfo.sType = VK_STRUCTURE_TYPE_FRAMEBUFFER_CREATE_INFO;
        framebufferInfo.renderPass = renderPass;
        framebufferInfo.attachmentCount = 1;
        framebufferInfo.pAttachments = attachments;
        framebufferInfo.width = env->swapChainExtent.width;
        framebufferInfo.height = env->swapChainExtent.height;
        framebufferInfo.layers = 1;

        if (vkCreateFramebuffer(env->logicalDevice, &framebufferInfo, nullptr, &swapChainFramebuffers[i]) != VK_SUCCESS)
        {
            throw std::runtime_error("failed to create framebuffer!");
        }
    }
}

void initVulkan()
{
    createRenderPass();
    createGraphicsPipeline();
    createFramebuffers();
}

void cleanUp()
{
    for (auto framebuffer : swapChainFramebuffers)
    {
        vkDestroyFramebuffer(env->logicalDevice, framebuffer, nullptr);
    }
    vkDestroyPipeline(env->logicalDevice, graphicsPipeline, nullptr);
    vkDestroyPipelineLayout(env->logicalDevice, pipelineLayout, nullptr);
    vkDestroyRenderPass(env->logicalDevice, renderPass, nullptr);
}